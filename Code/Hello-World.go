// This line informs the compiler that the file is the main project file and the entry point for the program
package main;

// Imports the library named 'fmt', which is basically the library for Input Output functions
import "fmt";

// This is the main function of the file, the Entry point.
func main() {
	// fmt.Printf is basically the equivalent of printf in C, it DOES NOT move the cursor to the next line, after printing the text which is provided
	fmt.Printf("Hello, World!");
}